/*assign03_StartUp01_knowing the truth value)*/

#include <iostream>
using namespace std;
int main()
{
	bool trueValue, falseValue;
	int x = 5, y = 10;
	trueValue = x < y;
	falseValue = x > y;
	cout << "True is " << trueValue << endl;
	cout << "False is " << falseValue;
	return 0;
}
// after running this program, it displays both true value 1 and false value 0;
